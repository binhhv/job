		var h = document.documentElement.clientHeight;//window.innerHeight;
 		h = (h*80)/100;
		//alert(h);
		if(w > 768){
			//alert("1");
			$(".about-page-box").css("min-height",h);
			var hR = $(".right-intro-about-us").height();
			$(".left-intro-about-us").height(hR);
			//alert(hleft);
		}
		setMaxHeightIdea(['#tab1','#tab2','#tab3','#tab4']);

	});

	function setMaxHeightIdea(tabs){
		var max = $(tabs[0]).height();
		for (var i = tabs.length - 1; i >= 1; i--) {
			if($(tabs[i]).height() > max){
				max = $(tabs[i]).height();
			}
		};
		//return max;
		for (var i = tabs.length - 1; i >= 0; i--) {
			$(tabs[i]).height(max);
		};
	}
